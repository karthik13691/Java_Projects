package com.javaoo.calculators;

public class BasicCalculator {
    /**
     *
     * @param num1
     * @param num2
     * @return sum of num1 and num2
     */

    public final double add(double num1, double num2) {
        return (num1 + num2) ;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return subtracted value of num1 and num2
     */

    public final double subtract(double num1, double num2) {
        return (num1 - num2) ;
    }
    /**
     *
     * @param num1
     * @param num2
     * @return product of num1 and num2
     */

    public final double multiply(double num1, double num2) {
        return (num1 * num2) ;
    }
    /**
     *
     * @param num1
     * @param num2
     * @return division of num1 and num2
     */

    public final double divide(double num1, double num2) {
        return (num1 / num2) ;
    }

}
